# + сложение addition:
# - вычитание substraction:
# * умножение multiplication:
# / деление division:
# % показывает остаток от деления.
# // целочисленное деление floor division:
# ** возведение в степень   exponent:
# ^ a ^ b вернет значение только с битами, установленными в a или в b, но не с обоими
a = 100
b = 210

print ("addition:\t" , a , "+" , b , "=" , a + b)

print ("substraction:\t" , a , "-" , b , "=" , a - b)

print ("multiplication:\t" , a , "*" , b , "=" , a * b)

print ("division:\t" , a , "/" , b , "=" , a / b)

print ("modulus:\t" , a , "%" , b , "=" , a % b)

print ("floor division:\t" , a , "//" , b , "=" , a // b)

print ("exponent:\t" , a , "**" , b , "=" , a ** b)







