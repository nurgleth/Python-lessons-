# оператор "and" это логическое "И"
# оператор "or" это логическое "ИЛИ"
# оператор "not" это логическое "НЕ"

a = True
b = False

print("AND Logick:")
print("a and a" , a and a)
print("a and b" , a and b)
print("b and b" , b and b)

print("\nOR Logick:")
print("a or a" , a or a)
print("a or b" , a or b)
print("b or b" , b or b)

print("\nNOT Logick:")
print("a=" , a , "\tnot a=", not a)
print("b=" , b , "\tnot b=", not b)
